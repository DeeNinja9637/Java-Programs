package deepak.pack;

public class pyramid {

	public static void main(String[] args) {
		int i, j;
		for (i=3; i>=1; i--) {
			for(j=i; j>=1; j--) {
				System.out.print(i);
			}
		System.out.println();
		}
		int k, l;
		for (k=2; k<=3; k++) {
			for(l=1; l<=k; l++) {
				System.out.print(k);
			}
		System.out.println();
		}

	}

}

